public class DisuseContractVo {
    private String contractId;
    private String disuseById;
    private String disuseByName;
    private String description;

    public String getContractId() {
        return contractId;
    }

    public void setContractId(String contractId) {
        this.contractId = contractId;
    }

    public String getDisuseById() {
        return disuseById;
    }

    public void setDisuseById(String disuseById) {
        this.disuseById = disuseById;
    }

    public String getDisuseByName() {
        return disuseByName;
    }

    public void setDisuseByName(String disuseByName) {
        this.disuseByName = disuseByName;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
